class Solution{
    public:
    Node* reverseLinkedList(Node* head) {
        Node* prev = nullptr;
        Node* curr = head;
        while (curr) {
            Node* nextNode = curr->next;
            curr->next = prev;
            prev = curr;
            curr = nextNode;
        }
        return prev;
    }
    struct Node* modifyTheList(struct Node *head)
    {
        Node* slow = head, *fast = head;
        while(fast && fast -> next) {
            slow = slow -> next;
            fast = fast -> next -> next;
        }
        Node* prev = head;
        while(prev -> next != slow) prev = prev -> next;
        prev -> next = reverseLinkedList(prev -> next);
        Node* cur = head, *mid = prev -> next;
        while(cur != prev -> next && mid) {
            int x = cur -> data;
            cur -> data = mid -> data - cur -> data;
            mid -> data = x;
            cur = cur -> next;
            mid = mid -> next;
        }
        prev -> next = reverseLinkedList(prev -> next);
        return head;
    }
};

