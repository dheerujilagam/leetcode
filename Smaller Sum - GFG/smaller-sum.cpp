class Solution{
public:
    vector<long long> smallerSum(int n,vector<int> &arr){
        // Code here
        vector<long long> dp(1e5 + 2, 0);
        for(int it : arr) dp[it + 1] += it;
        for(long long i = 2; i <= 1e5 + 1; i++) dp[i] += dp[i - 1];
        vector<long long> res(n, 0);
        for(int i = 0; i < n; i++) res[i] = dp[arr[i]];
        return res;
    }
};

