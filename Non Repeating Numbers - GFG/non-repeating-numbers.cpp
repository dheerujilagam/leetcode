class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
        int ans = 0;
        for(int it : nums) ans ^= it;
        int l = 0, r = 0;
        ans = ans & ~(ans - 1);
        for(int it : nums) {
            if(ans & it) l ^= it;
            else r ^= it;
        }
        return {min(l, r), max(l, r)};
    }
};

