class Solution {
  public:
    int minimumInteger(int n, vector<int> &a) {
        // code here
        long sum = 0, ans = LONG_MAX;
        for(long it : a) sum += it ;
        for(long it : a) {
            if(sum <= (n * it) && it < ans) ans = it;
        }
        return ans;
    }
};


